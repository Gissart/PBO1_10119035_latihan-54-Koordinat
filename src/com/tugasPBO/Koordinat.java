package com.tugasPBO;

public class Koordinat {
    protected int x,y;

    public Koordinat(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getx(){
        return x;
    }

    public void setX(){
        this.x = x;
    }

    public int getY(){
        return y;
    }

    public void setY(){
        this.y = y;
    }
}
